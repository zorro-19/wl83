static void *dev = NULL;
static u8 bindex = 0xff;
static u16 offset = 0;
static u8 key_tone_table;
static u8 kvoice_disable = 0;
static OS_SEM sem;
static int key_voice_init();

static void audio_dec_irq_handler(void *priv, void *data, int len)
{
    int rlen = len;
    int total_len;
    const u8 *table;
    if (bindex == 0xff) {
        return;
    }
    if (key_tone_table == 0) {
        table = key_voice;
        total_len = sizeof(key_voice);
    } else {
        table = take_photo_voice;
        total_len = sizeof(take_photo_voice);
    }

    if (offset == total_len) {
        return;
    }

    if (rlen > total_len - offset) {
        rlen = total_len - offset;
    }
    memcpy(data, table + offset, rlen);
    if (rlen < len) {
        memset((u8 *)data + rlen, 0, len - rlen);
    }
    offset += rlen;
    if (offset == total_len) {
        os_sem_set(&sem, 0);
        os_sem_post(&sem);
    }
}

void audio_off_task()
{
    while (1) {
        os_sem_pend(&sem, 0);
        if (bindex != 0xff) {
            dev_ioctl(dev, AUDIOC_STREAM_OFF, bindex);
            bindex = 0xff;
        }
        offset = 0;


    }
}

static int key_event_handler(struct sys_event *e)
{
    if (!db_select("kvo")) {
        return -1;
    }
    if (kvoice_disable) {
        return -1;
    }
    struct key_event *key = (struct key_event *)e->payload;
    if (key->action != KEY_EVENT_DOWN) {
        return -1;
    }
    if (key->value == KEY_PHOTO) {
        return -1;
    }
    if (dev == NULL) {
        key_voice_init();
    }
    offset = 0;
    key_tone_table = 0;
    if (bindex == 0xff) {
        dev_ioctl(dev, AUDIOC_SET_VOLUME, 100);
        dev_ioctl(dev, AUDIOC_STREAM_ON, (u32)&bindex);
    }
    return 0;
}
