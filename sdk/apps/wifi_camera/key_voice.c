static struct server *audio = NULL;
static FILE *file;
static void audio_server_event_handler(void *priv, int argc, int *argv)
{
    union audio_req r;
    switch (argv[0]) {
    case AUDIO_SERVER_EVENT_END:
        if (audio) {
            r.dec.cmd = AUDIO_DEC_STOP;
            server_request(audio, AUDIO_REQ_DEC, &r);
            server_close(audio);
            fclose(file);
            audio = NULL;
            kvoice_disable = 0;
        }
        break;
    }
}

void play_voice_file(const char *file_name)
{
    union audio_req r = {0};

    if (audio) {
        log_e("audio return ");
        return;
    }

    file = fopen(file_name, "r");
    if (!file) {
        log_e("open voice file err %s", file_name);
        return;
    }

    audio = server_open("audio_server", "dec");
    if (!audio) {
        log_e("audio server open fail");
        fclose(file);
        return;
    }
    server_register_event_handler(audio, NULL, audio_server_event_handler);

    log_d("start play voice %s", file_name);
    kvoice_disable = 1;

    r.dec.cmd             = AUDIO_DEC_OPEN;
    r.dec.volume          = 100;
    r.dec.output_buf      = NULL;
    r.dec.output_buf_len  = 8 * 1024;
    r.dec.file            = file;
    r.dec.channel         = 1;
    r.dec.sample_rate     = 8000;
    r.dec.priority        = 0;
    server_request(audio, AUDIO_REQ_DEC, &r);

    r.dec.cmd = AUDIO_DEC_START;
    server_request(audio, AUDIO_REQ_DEC, &r);
}


void stop_play_voice_file()
{
    union audio_req r;

    if (audio) {
        r.dec.cmd = AUDIO_DEC_STOP;
        server_request(audio, AUDIO_REQ_DEC, &r);
        server_close(audio);
        fclose(file);
        audio = NULL;
        kvoice_disable = 0;
    }
}



