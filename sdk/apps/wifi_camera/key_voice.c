void key_voice_start(int id)
{
    if (!db_select("kvo")) {
        return;
    }
    if (kvoice_disable) {
        return;
    }

#if 1
    if (dev == NULL) {
        key_voice_init();
    }
#endif

    offset = 0;
    key_tone_table = id;

    if (bindex == 0xff) {
        dev_ioctl(dev, AUDIOC_SET_VOLUME, 100);
        dev_ioctl(dev, AUDIOC_STREAM_ON, (u32)&bindex);
    }
}



static int key_voice_init()
{
    int err;
    os_sem_create(&sem, 0);
    struct audio_format f;

    dev = dev_open("audio", (void *)AUDIO_TYPE_DEC);
    if (!dev) {
        return 0;
    }

    f.volume        = 100;
    f.channel       = 1;
    f.sample_rate   = 8000;
    f.priority      = 4;
    f.sample_source = "dac";
    err = dev_ioctl(dev, AUDIOC_SET_FMT, (u32)&f);
    if (err) {
        puts("format_err\n");
        return 0;
    }

    struct audio_irq_handler parm;
    parm.irq_handler = audio_dec_irq_handler;
    parm.priv = NULL;
    dev_ioctl(dev, AUDIOC_REGISTER_IRQ_HANDLER, (u32)&parm);
    thread_fork("audio_irqoff_task", 29, 512, 0, 0, audio_off_task, NULL);
    return 0;
}
