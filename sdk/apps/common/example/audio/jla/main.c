//编码器输出JLA数据
static int pcm_vfs_fwrite(void *file, void *data, u32 len)
{
    //拿到数据填入cbuf
    cbuffer_t *cbuf = (cbuffer_t *)file;
    while (!cbuf_write(cbuf, data, len)) {
#if 1
        //如果写不进去, 等待数据读取 (要求完整性)
        os_sem_pend(&w_sem, 200);
#else
        //如果写不进去了,则清空一下cbuf(这一步目的为了不让接收数据有延迟,要求实时性)
        cbuf_clear(cbuf);
#endif
    }
    os_sem_set(&r_sem, 0);
    os_sem_post(&r_sem);

    return len;
}

//解码器读取数据,这里将数据传入JLA解码器,数据需要给足len的长度
static int pcm_vfs_fread(void *file, void *data, u32 len)
{
    //从cbuf取数据
    cbuffer_t *cbuf = (cbuffer_t *)file;
    do {
        if (cbuf_read(cbuf, data, len)) {
            os_sem_set(&w_sem, 0);
            os_sem_post(&w_sem);
            break;
        }
        os_sem_pend(&r_sem, 200);
        if (!enc_flag) {
            return 0;
        }
        if (!run_flag) {
            return 0;
        }
    } while (run_flag);


    return len;
}

static int pcm_vfs_fclose(void *file)
{
    return 0;
}

static int pcm_vfs_flen(void *file)
{
    return 0;
}

static const struct audio_vfs_ops pcm_vfs_ops = {
    .fwrite = pcm_vfs_fwrite,
    .fread  = pcm_vfs_fread,
    .fclose = pcm_vfs_fclose,
    .flen   = pcm_vfs_flen,

};



