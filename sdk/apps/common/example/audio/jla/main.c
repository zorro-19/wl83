static int test_dec_close(void)
{
    union audio_req req = {0};

    run_flag = 0;
    thread_kill(&virtual_pid, KILL_WAIT);
    if (dec_server) {
        req.dec.cmd = AUDIO_DEC_STOP;
        server_request(dec_server, AUDIO_REQ_DEC, &req);
    }
    if (cahe_buf) {
        free(cahe_buf);
    }
    if (pcm_fd) {
        fclose(pcm_fd);
    }
    if (JLA_INT24bit_INOUT && sin_data) {
        free(sin_data);
    }
    virtual_hdl = NULL;
    return 0;
}

