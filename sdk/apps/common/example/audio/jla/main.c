static int pcm_input(u8 *buf, u32 len)
{
    int rlen = len;
    int offset = 0;;
    int remain = 0;
    while (rlen) {
        remain = sin_data_len - data_offset;
        if (remain >= rlen) {
            memcpy(buf + offset, (u8 *)sin_data + data_offset, rlen);
            data_offset += rlen;
            offset += rlen;
            rlen = 0;
        } else {
            memcpy(buf + offset, (u8 *)sin_data + data_offset, remain);
            offset += remain;
            data_offset = 0;
            rlen -= remain;
        }
        if (!run_flag) {
            return 0;
        }
    }

    return len;
}


